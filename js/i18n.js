        t: function(key, fallback) {
            var lang = this.currentLang;
            if (translations[lang] && translations[lang][key] !== undefined) {
                return translations[lang][key];
            }
            if (translations.en[key] !== undefined) {
                return translations.en[key];
            }
            return fallback !== undefined ? fallback : key;
        },

        setLanguage: function(lang) {
            if (!translations[lang]) return;
            this.currentLang = lang;
            try { localStorage.setItem('webbuilder_lang', lang); } catch(e) {}
            this._applyToDOM();
            WB.render();
            // Re-render component panel (event listeners preserved via _eventsAttached flag)
            var list = document.getElementById('component-list');
            if (list) WB.ComponentPanel.init();
            // Update lang button label
            var currentEl = document.getElementById('lang-current');
            if (currentEl) currentEl.textContent = this.langMeta[lang] ? this.langMeta[lang].flag : lang.toUpperCase();
            // Update active state in dropdown
            var opts = document.querySelectorAll('.lang-option');
            for (var i = 0; i < opts.length; i++) {
                opts[i].classList.toggle('active', opts[i].getAttribute('data-lang') === lang);
            }
        },

        _applyToDOM: function() {
            // Text content
            var els = document.querySelectorAll('[data-i18n]');
            for (var i = 0; i < els.length; i++) {
                var key = els[i].getAttribute('data-i18n');
                els[i].textContent = this.t(key);
            }
            // Placeholder attributes
            var phEls = document.querySelectorAll('[data-i18n-placeholder]');
            for (var j = 0; j < phEls.length; j++) {
                var phKey = phEls[j].getAttribute('data-i18n-placeholder');
                phEls[j].placeholder = this.t(phKey);
            }
        },

        init: function() {
            try {
                var saved = localStorage.getItem('webbuilder_lang');
                if (saved && translations[saved]) this.currentLang = saved;
            } catch(e) {}
            this._applyToDOM();
        }
